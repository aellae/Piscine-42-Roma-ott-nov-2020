int		ft_sqrt(int nb)
{
	int i;

	i = 2;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (nb % i)
		i++;
	if ((nb / i) % i)
		return (0);
	return (i * ft_sqrt(nb / (i * i)));
}
